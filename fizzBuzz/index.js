// fizzBuzz in js

function fizzBuzz(n) {
  if (typeof n !== 'number') {
    console.log('That is not a number. Please enter a number.');
  }

  for (let i = 1; i <= n; i++) {
    if (i % 3 === 0 && i % 5 === 0) {
      console.log('FizzBuzz');
    } else if (i % 3 === 0) {
      console.log('Fizz');
    } else if (i % 5 === 0) {
      console.log('Buzz');
    } else {
      console.log(i);
    }
  }
}
